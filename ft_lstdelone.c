#include "libft.h"
#include <stdlib.h>

void	ft_lstdelone(t_list *lst, void (*del)(void *))
{
	t_list	*tmp;

	tmp = lst->next;
	lst->next = tmp;
	if (lst == NULL)
		return ;
	del(lst->content);
	free(lst);
}
